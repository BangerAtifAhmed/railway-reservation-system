CREATE TABLE IF NOT EXISTS ticket (
    pnr_no VARCHAR(10) PRIMARY KEY,
    train_no VARCHAR(10) NOT NULL,
    passenger_name VARCHAR(100) NOT NULL,
    date_time DATETIME NOT NULL,
    source_station VARCHAR(10) NOT NULL,
    destination_station VARCHAR(10) NOT NULL,
    status ENUM('booked', 'confirmed', 'cancelled', 'waiting') NOT NULL DEFAULT 'booked',
    user_id VARCHAR(10) NOT NULL,
    fare DECIMAL(10,2) CHECK (fare >= 0),
    booking_time DATETIME NOT NULL,
    cancellation_time DATETIME NULL,
    refund_amount DECIMAL(10,2) DEFAULT 0,
    FOREIGN KEY (train_no) REFERENCES train(train_no),
    FOREIGN KEY (source_station) REFERENCES station(station_id),
    FOREIGN KEY (destination_station) REFERENCES station(station_id),
    FOREIGN KEY (user_id) REFERENCES user(user_id)
);

CREATE TABLE IF NOT EXISTS payment (
    transaction_id VARCHAR(10) PRIMARY KEY,
    pnr_no VARCHAR(10) NOT NULL,
    user_id VARCHAR(10) NOT NULL,
    amount DECIMAL(10,2) NOT NULL,
    type ENUM('payment', 'refund') NOT NULL,
    mode ENUM('credit_card','debit_card','upi','net_banking','wallet') NOT NULL,
    status ENUM('success','failed','pending') NOT NULL,
    transaction_date DATETIME NOT NULL,
    FOREIGN KEY (pnr_no) REFERENCES ticket(pnr_no) ON DELETE CASCADE,
    FOREIGN KEY (user_id) REFERENCES user(user_id) ON DELETE CASCADE
);

-- History Tables
CREATE TABLE IF NOT EXISTS user_booking_history (
    history_id VARCHAR(10) PRIMARY KEY,
    user_id VARCHAR(10) NOT NULL,
    pnr_no VARCHAR(10) NOT NULL,
    action ENUM('booked', 'cancelled', 'modified') NOT NULL,
    action_time DATETIME NOT NULL,
    details TEXT,
    FOREIGN KEY (user_id) REFERENCES user(user_id) ON DELETE CASCADE,
    FOREIGN KEY (pnr_no) REFERENCES ticket(pnr_no) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS user_transaction_history (
    transaction_history_id VARCHAR(10) PRIMARY KEY,
    user_id VARCHAR(10) NOT NULL,
    pnr_no VARCHAR(10) NOT NULL,
    transaction_id VARCHAR(10) NOT NULL,
    transaction_type ENUM('payment', 'refund') NOT NULL,
    amount DECIMAL(10,2) NOT NULL,
    transaction_time DATETIME NOT NULL,
    status ENUM('success', 'failed', 'pending') NOT NULL,
    description TEXT,
    FOREIGN KEY (user_id) REFERENCES user(user_id) ON DELETE CASCADE,
    FOREIGN KEY (pnr_no) REFERENCES ticket(pnr_no) ON DELETE CASCADE,
    FOREIGN KEY (transaction_id) REFERENCES payment(transaction_id) ON DELETE CASCADE
);

-- Train Assignment Table
CREATE TABLE IF NOT EXISTS train_assignment (
    assignment_id VARCHAR(10) PRIMARY KEY,
    train_no VARCHAR(10) NOT NULL,
    employee_id VARCHAR(10) NOT NULL,
    assignment_date DATE NOT NULL,
    role ENUM('driver', 'guard', 'attendant', 'train_manager', 'ticket_checker', 'admin', 'support_staff', 'station_master') NOT NULL,
    FOREIGN KEY (train_no) REFERENCES train(train_no) ON DELETE CASCADE,
    FOREIGN KEY (employee_id) REFERENCES employee(employee_id) ON DELETE CASCADE,
    UNIQUE KEY unique_train_employee (train_no, employee_id, assignment_date)
);

CREATE TABLE IF NOT EXISTS allocates (
    allocation_id VARCHAR(10) PRIMARY KEY,
    pnr_no VARCHAR(10) NOT NULL,
    class_id VARCHAR(10) NOT NULL,
    berth_id VARCHAR(10), -- Can be NULL for waiting list
    allocation_status ENUM('confirmed', 'waiting', 'rac', 'cancelled') NOT NULL,
    allocation_time DATETIME NOT NULL,
    
    -- Foreign keys
    FOREIGN KEY (pnr_no) REFERENCES ticket(pnr_no) ON DELETE CASCADE,
    FOREIGN KEY (class_id) REFERENCES class(class_id) ON DELETE CASCADE,
    
    

    UNIQUE KEY unique_ticket_allocation (pnr_no) -- One ticket can have only one allocation
);

      
CREATE TABLE employee_booking_history (
    booking_id VARCHAR(10) PRIMARY KEY,
    employee_id VARCHAR(10) NOT NULL,
    pnr_no VARCHAR(10) NOT NULL,
    passenger_name VARCHAR(100) NOT NULL,
    train_no VARCHAR(10) NOT NULL,
    from_station VARCHAR(10) NOT NULL,
    to_station VARCHAR(10) NOT NULL,
    travel_date DATE NOT NULL,
    class_id VARCHAR(10) NOT NULL,
    booking_status ENUM('confirmed','waiting','cancelled') NOT NULL,
    booking_time DATETIME NOT NULL,
    cancellation_time DATETIME NULL,
    FOREIGN KEY (employee_id) REFERENCES employee(employee_id),
    FOREIGN KEY (pnr_no) REFERENCES ticket(pnr_no),
    FOREIGN KEY (train_no) REFERENCES train(train_no)
); 

CREATE TABLE employee_transaction_history (
    transaction_id VARCHAR(10) PRIMARY KEY,
    employee_id VARCHAR(10) NOT NULL,
    pnr_no VARCHAR(10) NOT NULL,
    transaction_type ENUM('free_booking', 'cancellation'),
    transaction_time DATETIME NOT NULL,
    description TEXT,
    FOREIGN KEY (employee_id) REFERENCES employee(employee_id),
    FOREIGN KEY (pnr_no) REFERENCES ticket(pnr_no)
);    
