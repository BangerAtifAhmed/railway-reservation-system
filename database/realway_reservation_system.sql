
CREATE DATABASE IF NOT EXISTS railway_reservation_system;
USE railway_reservation_system;

-- Core Tables
CREATE TABLE IF NOT EXISTS user (
    user_id VARCHAR(10) PRIMARY KEY,
    user_name VARCHAR(50) NOT NULL UNIQUE,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(100) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    dob DATE NOT NULL,
    age INT CHECK (age >= 0),
    gender ENUM('male','female','other'),
    address TEXT,
    city VARCHAR(50),
    state VARCHAR(50),
    pin_code VARCHAR(10)
);



CREATE TABLE IF NOT EXISTS employee (
    employee_id VARCHAR(10) PRIMARY KEY,
    emp_name VARCHAR(100) NOT NULL,
    designation ENUM('station_master', 'ticket_checker', 'train_manager', 'admin', 'support_staff') NOT NULL,
    email VARCHAR(100) NOT NULL UNIQUE,
    phone_no VARCHAR(15) NOT NULL,
    hire_date DATE NOT NULL,
    salary DECIMAL(10,2) CHECK (salary >= 0),
    supervisor_id VARCHAR(10),
    FOREIGN KEY (supervisor_id) REFERENCES employee(employee_id) ON DELETE SET NULL
);
ALTER TABLE employee ADD COLUMN password VARCHAR(255) NOT NULL DEFAULT 'default_password';
CREATE TABLE IF NOT EXISTS dependent (
    dependent_id VARCHAR(10),
    employee_id VARCHAR(10),
    f_name VARCHAR(50) NOT NULL,
    l_name VARCHAR(50) NOT NULL,
    dob DATE NOT NULL,
    gender ENUM('male','female','other'),
    relation ENUM('spouse','child','parent','sibling') NOT NULL,
    PRIMARY KEY (dependent_id, employee_id),
    FOREIGN KEY (employee_id) REFERENCES employee(employee_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS train (
    train_no VARCHAR(10) PRIMARY KEY,
    type ENUM('passenger','express','superfast','shatabdi','rajdhani','duronto','vande_bharat','garib_rath') NOT NULL,
    color ENUM('blue','red','green','yellow','white','orange','grey') NOT NULL,
    base_fare_multiplier DECIMAL(5,2) DEFAULT 1.0 CHECK (base_fare_multiplier >= 0)
);

CREATE TABLE IF NOT EXISTS station (
    station_id VARCHAR(10) PRIMARY KEY,
    station_name VARCHAR(100) NOT NULL,
    city VARCHAR(50) NOT NULL,
    state VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS schedule (
    schedule_id VARCHAR(10) PRIMARY KEY,
    train_no VARCHAR(10) NOT NULL,
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    no_of_stops INT CHECK (no_of_stops >= 0),
    FOREIGN KEY (train_no) REFERENCES train(train_no) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS running_days (
    schedule_id VARCHAR(10),
    day_name ENUM('monday','tuesday','wednesday','thursday','friday','saturday','sunday') NOT NULL,
    PRIMARY KEY (schedule_id, day_name),
    FOREIGN KEY (schedule_id) REFERENCES schedule(schedule_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS route_stop (
    route_stop_id VARCHAR(10) PRIMARY KEY,
    schedule_id VARCHAR(10) NOT NULL,
    station_id VARCHAR(10) NOT NULL,
    stop_sequence INT NOT NULL CHECK (stop_sequence > 0),
    arrival_time TIME,
    departure_time TIME,
    halt_duration INT CHECK (halt_duration >= 0),
    distance_from_source DECIMAL(8,2) CHECK (distance_from_source >= 0),
    day_number INT DEFAULT 1 CHECK (day_number >= 1),
    FOREIGN KEY (schedule_id) REFERENCES schedule(schedule_id) ON DELETE CASCADE,
    FOREIGN KEY (station_id) REFERENCES station(station_id) ON DELETE CASCADE,
    UNIQUE KEY unique_stop_sequence (schedule_id, stop_sequence)
);

CREATE TABLE IF NOT EXISTS fare_rule (
    fare_rule_id VARCHAR(10) PRIMARY KEY,
    coach_type ENUM('sleeper','ac','general') NOT NULL,
    from_distance DECIMAL(8,2) NOT NULL CHECK (from_distance >= 0),
    to_distance DECIMAL(8,2) NOT NULL CHECK (to_distance >= 0),
    rate_per_km DECIMAL(8,2) NOT NULL CHECK (rate_per_km >= 0),
    minimum_fare DECIMAL(8,2) NOT NULL CHECK (minimum_fare >= 0)
);

CREATE TABLE IF NOT EXISTS class (
    class_id VARCHAR(10) PRIMARY KEY,
    class_name VARCHAR(50) NOT NULL UNIQUE,
    booked_seats INT CHECK (booked_seats >= 0),
    no_of_coaches INT CHECK (no_of_coaches > 0),
    coach_type ENUM('sleeper','ac','general') NOT NULL,
    c_multiplier DECIMAL(5,2) NOT NULL DEFAULT 1.0 CHECK (c_multiplier >= 0),
    reservation_charges DECIMAL(8,2) DEFAULT 0 CHECK (reservation_charges >= 0),
    special_charges DECIMAL(8,2) DEFAULT 0 CHECK (special_charges >= 0),
    train_no VARCHAR(10) NOT NULL,
    fare_rule_id VARCHAR(10) NOT NULL,
    FOREIGN KEY (train_no) REFERENCES train(train_no) ON DELETE CASCADE,
    FOREIGN KEY (fare_rule_id) REFERENCES fare_rule(fare_rule_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS berth (
    berth_id VARCHAR(10),
    class_id VARCHAR(10),
    berth_no INT NOT NULL CHECK (berth_no > 0),
    status ENUM('available','booked','reserved') NOT NULL DEFAULT 'available',
    coach_no INT NOT NULL CHECK (coach_no > 0),
    PRIMARY KEY(berth_id, class_id),
    FOREIGN KEY (class_id) REFERENCES class(class_id) ON DELETE CASCADE,
    UNIQUE KEY uq_berth_per_coach (class_id, coach_no, berth_no)
);

