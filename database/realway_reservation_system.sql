CREATE DEFINER=`root`@`localhost` FUNCTION `CalculateTicketFare`(
    p_class_id VARCHAR(20),
    p_train_no VARCHAR(20),
    p_source_station VARCHAR(20),
    p_dest_station VARCHAR(20)
) RETURNS decimal(10,2)
    READS SQL DATA
    DETERMINISTIC
BEGIN
    DECLARE fare_amount DECIMAL(10,2);
    DECLARE journey_distance DECIMAL(10,2);
    DECLARE class_multiplier DECIMAL(10,2);
    DECLARE reservation_charges DECIMAL(10,2);
    DECLARE special_charges DECIMAL(10,2);
    
    -- Get class details and calculate distance
    SELECT 
        (dest_rs.distance_from_source - src_rs.distance_from_source),
        c.c_multiplier,
        c.reservation_charges,
        c.special_charges
    INTO 
        journey_distance,
        class_multiplier,
        reservation_charges,
        special_charges
    FROM class c
    JOIN schedule s ON c.train_no = s.train_no
    JOIN route_stop src_rs ON s.schedule_id = src_rs.schedule_id
    JOIN route_stop dest_rs ON s.schedule_id = dest_rs.schedule_id
    WHERE c.class_id = p_class_id 
      AND c.train_no = p_train_no
      AND src_rs.station_id = p_source_station
      AND dest_rs.station_id = p_dest_station
      AND src_rs.stop_sequence < dest_rs.stop_sequence
    LIMIT 1;
    
    -- Calculate fare: (distance * multiplier) + reservation_charges + special_charges
    IF journey_distance IS NOT NULL THEN
        SET fare_amount = ROUND(
            (journey_distance * class_multiplier) + 
            reservation_charges + 
            special_charges, 
            2
        );
    ELSE
        SET fare_amount = NULL;
    END IF;
    
    RETURN fare_amount;
END//
