CREATE  PROCEDURE `search_trains_between_cities`(
    IN p_from_city VARCHAR(50),
    IN p_to_city   VARCHAR(50),
    IN p_journey_date DATE
)
BEGIN
    /*
      Assumptions:
      - running_days(day_name) has full lowercase names: monday..sunday
      - route_stop times are TIME only; we handle overnight by adding +1 day when needed
    */

    SELECT 
        t.train_no,
        CONCAT(t.train_no, ' - ', UPPER(t.type)) AS train_name,

        -- IDs + names for endpoints
        s1.station_id AS from_station_id,
        s1.station_name AS from_station,
        s1.city         AS from_city,

        s2.station_id AS to_station_id,
        s2.station_name AS to_station,
        s2.city         AS to_city,

        TIME_FORMAT(rs1.departure_time, '%H:%i') AS departure,
        TIME_FORMAT(rs2.arrival_time,   '%H:%i') AS arrival,

        -- duration in minutes across midnight
        TIMESTAMPDIFF(
            MINUTE,
            TIMESTAMP(p_journey_date, rs1.departure_time),
            CASE
                WHEN rs2.arrival_time >= rs1.departure_time
                    THEN TIMESTAMP(p_journey_date, rs2.arrival_time)
                ELSE
                    TIMESTAMP(p_journey_date + INTERVAL 1 DAY, rs2.arrival_time)
            END
        ) AS duration_minutes,

        -- duration as HH:MM:SS (always positive)
        SEC_TO_TIME(
            TIMESTAMPDIFF(
                SECOND,
                TIMESTAMP(p_journey_date, rs1.departure_time),
                CASE
                    WHEN rs2.arrival_time >= rs1.departure_time
                        THEN TIMESTAMP(p_journey_date, rs2.arrival_time)
                    ELSE
                        TIMESTAMP(p_journey_date + INTERVAL 1 DAY, rs2.arrival_time)
                END
            )
        ) AS duration,

        ROUND((rs2.distance_from_source - rs1.distance_from_source), 2) AS distance_km,

        -- clamp intermediate count to >= 0
        GREATEST(0, (
            SELECT COUNT(*)
            FROM route_stop rsi
            WHERE rsi.schedule_id = sch.schedule_id
              AND rsi.stop_sequence BETWEEN rs1.stop_sequence AND rs2.stop_sequence
        ) - 2) AS intermediate_stops,

        -- list of intermediate STATIONS (names)
        (
            SELECT GROUP_CONCAT(s.station_name ORDER BY rsi.stop_sequence SEPARATOR '  ')
            FROM route_stop rsi
            JOIN station s ON s.station_id = rsi.station_id
            WHERE rsi.schedule_id = sch.schedule_id
              AND rsi.stop_sequence > rs1.stop_sequence
              AND rsi.stop_sequence < rs2.stop_sequence
        ) AS intermediate_stations,

        -- OPTIONAL: list of intermediate STATION IDs (comma-separated)
        (
            SELECT GROUP_CONCAT(s.station_id ORDER BY rsi.stop_sequence SEPARATOR ',')
            FROM route_stop rsi
            JOIN station s ON s.station_id = rsi.station_id
            WHERE rsi.schedule_id = sch.schedule_id
              AND rsi.stop_sequence > rs1.stop_sequence
              AND rsi.stop_sequence < rs2.stop_sequence
        ) AS intermediate_station_ids,

        -- running days (as MON,TUE,...)
        (
            SELECT GROUP_CONCAT(
                UPPER(SUBSTRING(rd.day_name,1,3))
                ORDER BY FIELD(rd.day_name,'monday','tuesday','wednesday','thursday','friday','saturday','sunday')
            )
            FROM running_days rd
            WHERE rd.schedule_id = sch.schedule_id
        ) AS running_days,

        -- classes
        (
            SELECT GROUP_CONCAT(DISTINCT class_name ORDER BY class_name)
            FROM class c 
            WHERE c.train_no = t.train_no
        ) AS available_classes

    FROM train t
    JOIN schedule   sch ON t.train_no      = sch.train_no
    JOIN route_stop rs1 ON sch.schedule_id = rs1.schedule_id
    JOIN station    s1  ON rs1.station_id  = s1.station_id
    JOIN route_stop rs2 ON sch.schedule_id = rs2.schedule_id
    JOIN station    s2  ON rs2.station_id  = s2.station_id

    WHERE
        LOWER(s1.city) = LOWER(p_from_city)
        AND LOWER(s2.city) = LOWER(p_to_city)
        AND rs1.stop_sequence < rs2.stop_sequence

        -- ensure it runs on the requested date
        AND EXISTS (
            SELECT 1
            FROM running_days rd
            WHERE rd.schedule_id = sch.schedule_id
              AND rd.day_name = LOWER(DAYNAME(p_journey_date))
        )

    ORDER BY rs1.departure_time;
END//

